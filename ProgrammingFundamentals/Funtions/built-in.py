# Built-in 'len()' function to get the number of elements in a list or string
my_list = [1, 2, 3, 4]  # List containing 4 elements
my_string = "Hola Mundo"  # String with 10 characters
print(len(my_list))  # Output: 4 (number of elements in the list)
print(len(my_string))  # Output: 10 (number of characters in the string)

# Built-in 'sorted()' function to sort the elements in ascending order
numbers = [4, 2, 8, 1]  # List of numbers
words = ["manzana", "banana", "cereza"]  # List of words
print(sorted(numbers))  # Output: [1, 2, 4, 8] (numbers sorted in ascending order)
print(sorted(words))  # Output: ['banana', 'cereza', 'manzana'] (words sorted alphabetically)
